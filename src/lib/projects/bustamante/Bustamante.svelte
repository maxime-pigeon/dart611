<p>
	The project is an online web-based platform that uses a sole numeral as the
	main interface, with the story connected to it hidden on a separate, hidden
	layer. The objective is to instigate the curiosity and interest of the
	audience for the impact of the graphic sign of the number itself, by
	stripping the message of all content, except the numerical one. It is
	thought of as a participative, co-design platform that invites the audience
	to input numbers that are resonant to their lives, and to share their
	meanings. Those numbers and messages entered will be fed into the database
	and will be shown to the audience on the interface being designed.
</p>

