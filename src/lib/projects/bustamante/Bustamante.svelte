<p>What information can numbers communicate?</p>

<p>
	COUNTLESS is a project in the realm of information design, typography,
	interaction and web design, that investigates what stories numbers carry and
	communicate. What is hidden behind a sole numeral? What meanings and
	significances it carries? Do you have numbers that are part of your story
	and that help situate your positionality?
</p>

<a
	href="https://hybrid.concordia.ca/a_ouza/countless/index.html"
	target="blank"
>
	<img srcset={Image2} alt="Image2" />
</a>

<p>
	It is a project to explore personal or collective connotations and
	connections represented in numerals. What they tell, and what lies hidden.
	It plays with information design and the readiness of content that is
	displayed in infographics and distorts the ways we interact with that
	content. In infographics, numbers are part of a message that associates
	forms, shapes and text to convey its meaning. In this project, they are
	presented first naked of any complementary subject.
</p>

<p>
	Data, often numerical, is the core of information design. By revealing only
	the numerical layer, with no direct explanation connected, a wide range of
	significance can be attributed to numbers. From another perspective, sole
	numbers are also a way to strip all the meaning and direct attention to the
	medium, as Jasper Johns does in his work 0 to 9, for example. Attempting to
	reach the essence is not a strange concept in art history—we could argue
	that such a description encompasses much of the modernist movement. This is
	an experiment that exaggerates modernist intentions to reflect upon
	communication, empathy and effectiveness of numbers.
</p>

<a
	href="https://hybrid.concordia.ca/a_ouza/countless/index.html"
	target="blank"
>
