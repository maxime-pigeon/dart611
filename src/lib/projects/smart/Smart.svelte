<p>
	Each of the books described above was ‘written’ by Open AI’s GPT3 text
	transformer. With help from researcher and artist Alexandre Reben, a
	selection of artist bookworks were described in the text, altered to
	integrate post-consumer and salvaged materials, then fed into GPT3 for
	training. The algorithm, trained on vast quantities of web-based literature,
	identifies patterns in the language used in these prepared inductive
	descriptions, and with each carriage return, a new description of a bookwork
	is born. Selected bookworks are collected here and presented in a book made
	entirely of domestic plastic waste—an exercise performed as a primer for the
	nature of bookworks resulting from this methodological arrangement.
</p>

<img srcset={Image2} alt="Image2" />

<p>
	This exercise provides a sense of what it feels like to work with AI, as
	well as how a working relationship may form between maker and machine to
	extend creative output and augment general creativity. Much like the
	heightened lateral thinking afforded by <i>Obliques Strategies</i>, a
	card-based method created by Brian Eno to promote creativity, working with
	textual transformers offer a wealth of prompts that may serve to support
	creative locomotion.
</p>

<p>
	This work also attempts to reflect on how we may balance our approach in
	working with hyperproductive machine intelligence by introducing Slow Design
	principles to our theoretical framework. Functioning as an engine to power
	reflection and expand on the potential of waste materials, this generator
	proposes what may be considered as a new solution to post-consumer waste
	with each return. Much like an embodied version of a Generative Adversarial
	Network, the cognitive labor of imagining a new material proposition has
	been automated by neural networks while human judgment has been reserved for
	the evaluation of the descriptions.
</p>

<img srcset={Image3} alt="Image3" />

<p>
	Specifically considering how bookarts and independent publishing may be
	transformed with the introduction of machine intelligence, the broader
	narratives presented by this work will invite the viewer to question
	creative license and artistic agency in a machine-supported studio practice.
	While these bookworks may appear incomplete or to have failed in
	ways—missing content, or following literal interpretations of incoherent
	instructions—this quality lends to transparency and honesty in sharing the
	playful and exploratory process of working with this nascent artistic
	collaborator.
</p>

